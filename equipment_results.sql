SELECT 
    m.ID,
    m.RECORD_NAME,
    m.RAW_DATA,
    DBMS_LOB.SUBSTR(m.RAW_DATA_LONG_TEXT, 4000, 1) as RAW_DATA_PREVIEW,
    ms.SAMPLE_ID,
    m.MEASUREMENT_TYPE
FROM RES_MEASUREMENT m
JOIN RES_MEASUREMENTSAMPLE ms ON m.ID = ms.MEASUREMENT_ID
WHERE ms.SAMPLE_ID = 'S001'
AND ROWNUM <= 5;